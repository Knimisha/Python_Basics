# fetch substring by giving index
string1 = "Coke studio"
print(string1[2])

#fetching a substring by giving starting and end index. In this case the last index gets excluded
print(string1[0:2])

#fetching a substring by giving only starting index. It will fetch string from 3rd character till last
print(string1[3:])

#fetching a substring by giving end index. It will fetch string from the beginning till the end index mentioned
print(string1[:9])

